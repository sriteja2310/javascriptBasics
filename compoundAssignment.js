var a = 3;
var b = 10;
var c = 2;

//regular way of performing addition
a = a + 10;
console.log(a);
// also can be done with compound assignment with augmented addition i.e a = a + 10; is also performed by a+=10;
a +=10;
console.log(a);

//regular way of performing subtraction
b = b - 1;
console.log(b);
// also can be done with compound assignment with augmented subtraction i.e b = b - 1; is also performed by b -=1;
b -=1;
console.log(b);

//regular way of performing multiplication
c = c*2;
console.log(c);
// also can be done with compound assignment with augmented multiplication i.e c = c*2; is also performed by c *= 2;
c *= 2;
console.log(c);

